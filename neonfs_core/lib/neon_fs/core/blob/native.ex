defmodule NeonFS.Core.Blob.Native do
  @moduledoc """
  Native Rust NIF bindings for blob storage operations.

  This module provides low-level bindings to the neonfs_blob Rust crate,
  which handles content-addressed chunk storage, chunking algorithms,
  compression, and encryption.
  """

  use Rustler,
    otp_app: :neonfs_core,
    crate: :neonfs_blob

  @doc """
  Test function that adds two integers.

  This is the default NIF function generated by Rustler for verification.
  It will be replaced with actual blob storage functions in subsequent tasks.
  """
  @spec add(integer(), integer()) :: integer()
  def add(_a, _b), do: :erlang.nif_error(:nif_not_loaded)

  @doc """
  Computes the SHA-256 hash of the given binary data.

  The hash is computed on the raw data as provided - no compression or
  encryption is applied. This ensures consistent content addressing
  regardless of storage format.

  ## Parameters
    - `data` - Binary data to hash

  ## Returns
    - A 32-byte binary containing the SHA-256 hash

  ## Examples

      iex> hash = NeonFS.Core.Blob.Native.compute_hash("hello world")
      iex> byte_size(hash)
      32

  """
  @spec compute_hash(binary()) :: binary()
  def compute_hash(_data), do: :erlang.nif_error(:nif_not_loaded)
end
